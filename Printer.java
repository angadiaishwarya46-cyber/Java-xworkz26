class Printer {

   public static void getBrand() {
      System.out.println("The printer brand is Canon");
   }

   public static void getModel() {
      System.out.println("The printer model is MG2570S");
   }

   public static void getPrice() {
      System.out.println("The printer price is Rs. 3,599");
   }

   public static void getType() {
      System.out.println("The printer type is Inkjet");
   }

   public static void main(String[] arg) {
      getBrand();
      getModel();
      getPrice();
      getType();
   }
}